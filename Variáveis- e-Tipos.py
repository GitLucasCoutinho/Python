# Inteiro (int)
idade = 25
print("Idade:", idade)

# Número decimal (float)
altura = 1.75
print("Altura:", altura)

# Texto (string)
nome = "Lucas"
print("Nome:", nome)

# Booleano (bool)
estudando = True
print("Está estudando?", estudando)

# Lista (list)
frutas = ["maçã", "banana", "laranja"]
print("Lista de frutas:", frutas)

# Tupla (tuple)
coordenadas = (10, 20)
print("Coordenadas:", coordenadas)

# Dicionário (dict)
pessoa = {"nome": "Lucas", "idade": 25, "cidade": "Jaú"}
print("Dicionário pessoa:", pessoa)

# Conjunto (set)
numeros_unicos = {1, 2, 3, 3, 2}
print("Conjunto de números únicos:", numeros_unicos)