# Programa de operações matemáticas

# Declaração das variáveis
num1 = 12
num2 = 4

# Operações
soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2  # divisão normal (float)

# Saída
print("Número 1:", num1)
print("Número 2:", num2)
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)